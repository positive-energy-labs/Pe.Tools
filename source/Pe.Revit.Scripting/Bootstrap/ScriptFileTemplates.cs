    public static string CreateSampleScript() =>
        """
        // Run from this workspace root:
        // pea script execute --source-path src\SampleScript.cs
        //
        // Loose workspace execution uses the selected file as the entrypoint.
        // Keep exactly one non-abstract PeScriptContainer in this file.

        public sealed class SampleScript : PeScriptContainer
        {
            public override void Execute()
            {
                WriteLine($"Running in Revit {revitVersion}.");

                var hostProbe = Host.Host.GetProbeAsync().GetAwaiter().GetResult();
                WriteLine($"Host contract: {hostProbe.HostContractVersion}.");

                if (doc == null)
                {
                    WriteLine("No active document.");
                    return;
                }

                var artifact = Artifacts.WriteJson("document-summary.json", new
                {
                    document = doc.Title,
                    revitVersion,
                    selectedElementCount = selection.Count
                });

                WriteLine($"Wrote artifact: {artifact.FullPath}");
            }
        }
        """;
