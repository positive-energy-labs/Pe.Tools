using System.Reflection;
using System.Text.Json;
using Pe.Shared.Product;

namespace Pe.Dev.Cli.Codegen;

internal static class ProductTypeScriptProjection {
    public static async Task<int> RunAsync(bool check, CodegenPaths paths, CancellationToken cancellationToken) {
        var generatedFile = GenerateFile(paths);
        if (check)
            return await CheckAsync(paths, generatedFile, cancellationToken);

        Directory.CreateDirectory(Path.GetDirectoryName(generatedFile.Path)!);
        await File.WriteAllTextAsync(generatedFile.Path, generatedFile.Content, cancellationToken);
        Console.WriteLine($"Generated {Path.GetRelativePath(paths.RepoRoot, generatedFile.Path)}");
        return 0;
    }

    private static async Task<int> CheckAsync(
        CodegenPaths paths,
        GeneratedProjectionFile generatedFile,
        CancellationToken cancellationToken
    ) {
        var relativePath = Path.GetRelativePath(paths.RepoRoot, generatedFile.Path);
        if (!File.Exists(generatedFile.Path)) {
            Console.Error.WriteLine("Generated Product TypeScript projection is stale:");
            Console.Error.WriteLine($"  {relativePath} (missing)");
            Console.Error.WriteLine("Run `pe-dev codegen sync --target product` to update it.");
            return 1;
        }

        var existingContent = await File.ReadAllTextAsync(generatedFile.Path, cancellationToken);
        if (string.Equals(existingContent, generatedFile.Content, StringComparison.Ordinal)) {
            Console.WriteLine("Generated Product TypeScript projection is current.");
            return 0;
        }

        Console.Error.WriteLine("Generated Product TypeScript projection is stale:");
        Console.Error.WriteLine($"  {relativePath}");
        Console.Error.WriteLine("Run `pe-dev codegen sync --target product` to update it.");
        return 1;
    }

    private static GeneratedProjectionFile GenerateFile(CodegenPaths paths) => new(
        Path.Combine(paths.HostContractsDirectory, "product.generated.ts"),
        NormalizeLineEndings(GenerateTypeScript())
    );

    private static string GenerateTypeScript() => $$"""
        // <auto-generated />
        // Generated by `pe-dev codegen sync --target product` from Pe.Shared.Product.

        {{RenderConstantObject("productIdentity", typeof(ProductIdentity))}}

        {{RenderConstantObject("productPathNames", typeof(ProductPathNames))}}

        {{RenderConstantObject("hostProcessIdentity", typeof(HostProcessIdentity))}}

        {{RenderConstantObject("peaCliIdentity", typeof(PeaCliIdentity))}}

        {{RenderConstantObject("peDevCliIdentity", typeof(PeDevCliIdentity))}}

        {{RenderConstantObject("revitDeploymentIdentity", typeof(RevitDeploymentIdentity))}}

        {{RenderConstantObject("peAppRuntimeDeploymentDescriptor", typeof(PeAppRuntimeDeploymentDescriptor))}}

        {{RenderConstantObject("installerPayloadManifest", typeof(InstallerPayloadManifest))}}

        {{RenderConstantObject("scriptingWorkspaceIdentity", typeof(ScriptingWorkspaceLayout))}}
        """;

    private static string RenderConstantObject(string exportName, Type sourceType) {
        var fields = sourceType
            .GetFields(BindingFlags.Public | BindingFlags.Static)
            .Where(field => field.IsLiteral && !field.IsInitOnly)
            .OrderBy(field => field.MetadataToken)
            .ToArray();

        if (fields.Length == 0)
            throw new InvalidOperationException($"Product projection source '{sourceType.FullName}' has no public constants.");

        var lines = fields.Select(field => {
            var value = field.GetRawConstantValue()
                ?? throw new InvalidOperationException($"Product projection constant '{sourceType.FullName}.{field.Name}' is null.");
            return $"  {ToCamelCase(field.Name)}: {Ts(value)},";
        });

        return string.Join(
            "\n",
            new[] { $"export const {exportName} = {{" }
                .Concat(lines)
                .Concat(["} as const;"])
        );
    }

    private static string ToCamelCase(string value) =>
        string.IsNullOrEmpty(value) || char.IsLower(value[0])
            ? value
            : char.ToLowerInvariant(value[0]) + value[1..];

    private static string Ts(object value) => JsonSerializer.Serialize(value);

    private static string NormalizeLineEndings(string content) {
        var normalized = content.Replace("\r\n", "\n").Replace("\r", "\n");
        return normalized.EndsWith('\n') ? normalized : normalized + "\n";
    }

    private sealed record GeneratedProjectionFile(string Path, string Content);
}
