    private static string RenderTypeScriptOperationGroup(
        string exportName,
        IReadOnlyList<GeneratedHostClientOperation> operations
    ) {
        var builder = new StringBuilder();
        _ = builder.AppendLine($"export const {exportName} = {{");
        foreach (var operation in operations) {
            _ = builder.AppendLine($"  {operation.MethodName}: {{");
            _ = builder.AppendLine($"    key: \"{operation.Definition.Key}\",");
            _ = builder.AppendLine($"    verb: \"{ToTypeScriptHttpVerb(operation.Definition.Verb)}\",");
            _ = builder.AppendLine($"    route: \"{operation.Definition.Route}\",");
            _ = builder.AppendLine($"    executionMode: \"{operation.Definition.ExecutionMode}\",");
            _ = builder.AppendLine("  },");
        }

        _ = builder.Append("} as const satisfies Record<string, HostOperationDefinition>;");
        return builder.ToString();
    }

    private static void ValidatePeaClientOperations() {
        var generatedKeys = HostClientOperations
            .Concat(ScriptingClientOperations)
            .Select(operation => operation.Definition.Key)
            .ToHashSet(StringComparer.Ordinal);
        var catalogKeys = HostOperationsCatalog.PeaClientSlice
            .Select(definition => definition.Key)
            .ToHashSet(StringComparer.Ordinal);

        var missingFromCatalog = generatedKeys.Except(catalogKeys, StringComparer.Ordinal).ToList();
        if (missingFromCatalog.Count != 0)
            throw new InvalidOperationException(
                $"Generated pea host client contains operations missing from HostOperationsCatalog.PeaClientSlice: {string.Join(", ", missingFromCatalog)}"
            );

        var missingFromGenerator = catalogKeys.Except(generatedKeys, StringComparer.Ordinal).ToList();
        if (missingFromGenerator.Count != 0)
            throw new InvalidOperationException(
                $"HostOperationsCatalog.PeaClientSlice contains operations missing from the generator: {string.Join(", ", missingFromGenerator)}"
            );
    }

    private static string ToTypeScriptHttpVerb(HostHttpVerb verb) => verb switch {
        HostHttpVerb.Get => "GET",
        HostHttpVerb.Post => "POST",
        _ => throw new InvalidOperationException($"Unsupported host operation HTTP verb '{verb}'.")
    };

    private sealed record GeneratedHostClientOperation(string MethodName, HostOperationDefinition Definition);

    private sealed record GeneratedPeaClientFile(string Path, string Content);

    private sealed record PeaHostClientSyncOptions(bool Check) {
        public static PeaHostClientSyncOptions Parse(IReadOnlyList<string> args) {
            var check = false;
            foreach (var arg in args) {
                switch (arg) {
                case "--check":
                    check = true;
                    break;
                default:
                    throw new ArgumentException($"Unknown sync-pea-host-client option '{arg}'. Supported options: --check.");
                }
            }

            return new PeaHostClientSyncOptions(check);
        }
    }
