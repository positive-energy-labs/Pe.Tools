internal static class PeaHostClientSyncCli {
    private static readonly GeneratedHostClientOperation[] HostClientOperations = [
        new("getProbe", GetHostProbeOperationContract.Definition),
        new("getSessionSummary", GetHostSessionSummaryOperationContract.Definition),
        new("getLogs", GetHostLogsOperationContract.Definition)
    ];

    private static readonly GeneratedHostClientOperation[] ScriptingClientOperations = [
        new("bootstrapWorkspace", GetScriptWorkspaceBootstrapOperationContract.Definition),
        new("execute", ExecuteRevitScriptOperationContract.Definition)
    ];

    public static async Task<int> RunAsync(
        IReadOnlyList<string> forwardedArguments,
        string? repoRootOverride,
        CancellationToken cancellationToken
    ) {
        PeaHostClientSyncOptions options;
        string repoRoot;
        try {
            options = PeaHostClientSyncOptions.Parse(forwardedArguments);
            ValidatePeaClientOperations();
            repoRoot = RepoRootResolver.Resolve(repoRootOverride);
        } catch (Exception ex) {
            Console.Error.WriteLine(ex.Message);
            return 10;
        }

        var typescriptPath = Path.Combine(repoRoot, "source", "pea", "app", "generated", "pe-host-client.ts");
        var generatedFiles = new[] {
            new GeneratedPeaClientFile(typescriptPath, GenerateTypeScriptClient())
        };

        if (options.Check)
            return await CheckAsync(repoRoot, generatedFiles, cancellationToken);

        foreach (var generatedFile in generatedFiles) {
            Directory.CreateDirectory(Path.GetDirectoryName(generatedFile.Path)!);
            await File.WriteAllTextAsync(generatedFile.Path, generatedFile.Content, cancellationToken);
            Console.WriteLine($"Generated {Path.GetRelativePath(repoRoot, generatedFile.Path)}");
        }

        return 0;
    }

    private static async Task<int> CheckAsync(
        string repoRoot,
        IReadOnlyList<GeneratedPeaClientFile> generatedFiles,
        CancellationToken cancellationToken
    ) {
        var staleFiles = new List<string>();
        foreach (var generatedFile in generatedFiles) {
            var relativePath = Path.GetRelativePath(repoRoot, generatedFile.Path);
            if (!File.Exists(generatedFile.Path)) {
                staleFiles.Add($"{relativePath} (missing)");
                continue;
            }

            var existingContent = await File.ReadAllTextAsync(generatedFile.Path, cancellationToken);
            if (!string.Equals(existingContent, generatedFile.Content, StringComparison.Ordinal))
                staleFiles.Add(relativePath);
        }

        if (staleFiles.Count == 0) {
            Console.WriteLine("Generated pea host client is current.");
            return 0;
        }

        Console.Error.WriteLine("Generated pea host client is stale:");
        foreach (var staleFile in staleFiles)
            Console.Error.WriteLine($"  {staleFile}");
        Console.Error.WriteLine("Run `pe-dev revit sync-pea-host-client` to update it.");
        return 1;
    }

